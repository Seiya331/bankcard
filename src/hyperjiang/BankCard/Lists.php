    /**
     * 根据银行代码获取银行名称
     *
     * @param  string $bankCode 银行代码
     * @return string
     */
    public static function getBankName($bankCode)
    {
        foreach (self::$banks as $bank) {
            if ($bank['code'] == strtoupper($bankCode)) {
                return $bank['name'];
            }
        }
        return '';
    }

    /**
     * 根据银行卡号获取卡信息
     *
     * @param  string $cardNo 银行卡号
     * @return array
     */
    public static function getInfo($cardNo)
    {
        foreach (self::$banks as $bank) {
            foreach ($bank['patterns'] as $pattern) {
                if (empty($pattern['reg'])) {
                    continue;
                }
                if (preg_match($pattern['reg'], $cardNo)) {
                    return [
                        'bankCode'     => $bank['code'],
                        'bankName'     => $bank['name'],
                        'cardType'     => $pattern['type'],
                        'cardTypeName' => Types::get($pattern['type']),
                    ];
                }
            }
        }

        return [];
    }

